// 页面加载时自动应用一次
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', applyI18n);
} else {
    applyI18n();
}

function getLocale() {
    const saved = localStorage.getItem(I18N_STORAGE_KEY);
    if (saved && i18nMessages[saved]) return saved;
    const nav = navigator.language || 'zh-CN';
    if (i18nMessages[nav]) return nav;
    if (nav.startsWith('zh')) return 'zh-CN';
    return 'en';
}

function setLocale(locale) {
    if (!i18nMessages[locale]) return;
    localStorage.setItem(I18N_STORAGE_KEY, locale);
    applyI18n();
    window.dispatchEvent(new CustomEvent('locale-changed', { detail: locale }));
}

function t(key, replacements) {
    const locale = getLocale();
    let text = i18nMessages[locale]?.[key] ?? i18nMessages['zh-CN']?.[key] ?? key;
    if (replacements) {
        Object.keys(replacements).forEach(k => {
            text = text.replace(new RegExp(`\\{${k}\\}`, 'g'), replacements[k]);
        });
    }
    return text;
}

function applyI18n(root = document) {
    const locale = getLocale();
    document.documentElement.lang = locale === 'zh-TW' ? 'zh-Hant' : (locale === 'en' ? 'en' : 'zh-Hans');
    
    root.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        const attr = el.dataset.i18nAttr;
        const text = t(key);
        if (attr) {
            el.setAttribute(attr, text);
        } else {
            if (el.children.length === 0) {
                el.textContent = text;
            } else {
                if (el.firstChild && el.firstChild.nodeType === Node.TEXT_NODE) {
                    el.firstChild.textContent = text;
                } else {
                    el.textContent = text;
                }
            }
        }
    });
    
    root.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        el.placeholder = t(el.dataset.i18nPlaceholder);
    });
    
    root.querySelectorAll('[data-i18n-title]').forEach(el => {
        el.title = t(el.dataset.i18nTitle);
    });
}

window.i18n = { getLocale, setLocale, t, applyI18n };
